 ========== BANCO DE DADOS ===========

 Conceito: Conjunto de dados organizados e estruturados.

 Tipos: Relacional e não relacional

 ======== BANCO DE DADOS RELACIONAL ==========

Conceito: É um conjunto de dados organizado em tabelas estruturadas e seguem um padrão pré definido, com regras de execução e consulta.
Prós: Consistência dos dados, integridade e dados estruturados.
Contra: Inflexível, alto custo de licenciamento e baixo desempenho para big data.
Linguagem: SQL (Structured Query Language).
Exemplos de bancos: Oracle, SQL Server e DB2.


  ======== BANCO DE DADOS NÃO RELACIONAL ==========

Conceito: É um conjunto de dados organizado em modelos de dados flexíveis geralmente sendo um dos três: chave-valor, documentos ou grafos, existindo ainda outros modelos.
Prós: Flexibilidade, alta escalabilidade, alto desempenho para big data e principalmente alta disponibilidade existindo diversos softwares com fácil replicação facilitando em casos de falhas.
Contra: Menor consistência, falta de padrão, alto custo de infraestrutura e necessidade de curva de apredizado para escolher o melhor modelo.
Linguagem: JSON, Redis, MQL (Mongo Query Language) para MongoDB.
Exemplos de bancos: MongoDB, Neo4j, Redis e Firebase Firestore.